/* Simple Calculator Demo */
#include <stdio.h>

int main() {
    int a = 10;
    int b = 5;
    
    printf("Simple Calculator Demo\n");
    printf("A = %d, B = %d\n", a, b);
    printf("A + B = %d\n", a + b);
    printf("A - B = %d\n", a - b);
    printf("A * B = %d\n", a * b);
    printf("A / B = %d\n", a / b);
    
    return 0;
}
